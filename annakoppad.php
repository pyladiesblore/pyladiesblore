  <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I am the founder of PyLadies Bangalore chapter, that was introduced to help women in Bangalore learn Python programming. Way back in the summer of July 2013, when I wanted to get back to coding. I looked up a couple of tutorials on the web, and then took up the course on Python programming at Udacity. When I stumbled upon roadblocks, I found that there were very little users or people that I could turn for help. 
  </p>
  <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; After a little googling, I found that there was an international community to support the women in programming sphere called <a href="http://www.pyladies.com/">PyLadies</a>. It was then I started the  Bangalore chapter of the parent <a href="http://www.pyladies.com/">PyLadies</a> group. 
  </p>
  <p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The chapter has been active over a couple of months and has been growing ever since.
  </p>
  <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; I am doing the first <a href="http://conference.pyladies.com/">PyLadies conference</a>, 2017 in Bangalore, India. Please look out the page <a href="http://conference.pyladies.com/">PyLadies conference</a> website for more details!!!
