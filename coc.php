<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Pyladies Bangalore is dedicated to providing a respectful, harassment-free community for everyone. We do not tolerate harassment or bullying of any community member in any form. This does not only extend to members to local Pyladies Bangalore communities, but to anyone who chooses to become involved in the larger Pyladies Bangalore community of users, developers and integrators through events or interactions.
</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Harassment includes offensive verbal/electronic comments related to personal characteristics or choices, sexual images or comments in public or online spaces, deliberate intimidation, bullying, stalking, following, harassing photography or recording, sustained disruption of talks, IRC chats, electronic meetings, physical meetings or other events, inappropriate physical contact, or unwelcome sexual attention. Participants asked to stop any harassing or bullying behavior are expected to comply immediately.
</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
If a participant engages in harassing behavior, representatives of the community may take reasonable action they deem appropriate, including warning the offender, expulsion from any Pyladies Bangalore event, or expulsion from mailing lists, IRC chats, discussion boards and other electronic communications channels to resolve the issue. This may include expulsion from Pyladies Bangalore Meetup group membership.
</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
If you are being harassed, notice that someone else is being harassed, or have any other concerns, please act to intercede or ask for help from any member of the Pyladies Bangalore community, IRC chat admins, website admins, or organizers/representatives of any physical events put on under the auspices of Pyladies Bangalore .
</p>
